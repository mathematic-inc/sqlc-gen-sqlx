use buffa::Message as _;
use sqlc_gen_sqlx::plugin::{
    Catalog, Column, GenerateRequest, Identifier, Parameter, Query, Schema, Table,
};
use sqlc_gen_sqlx::run_with_bytes;

fn make_author_column(name: &str, pg_type: &str, not_null: bool) -> Column {
    Column {
        name: name.to_string(),
        r#type: Some(Identifier {
            name: pg_type.to_string(),
            ..Default::default()
        })
        .into(),
        not_null,
        ..Default::default()
    }
}

fn authors_table() -> Table {
    Table {
        rel: Some(Identifier {
            name: "authors".to_string(),
            ..Default::default()
        })
        .into(),
        columns: vec![
            make_author_column("id", "int8", true),
            make_author_column("name", "text", true),
            make_author_column("bio", "text", false),
        ],
        ..Default::default()
    }
}

fn make_request(queries: Vec<Query>) -> Vec<u8> {
    let req = GenerateRequest {
        catalog: Some(Catalog {
            default_schema: "public".to_string(),
            schemas: vec![Schema {
                name: "public".to_string(),
                tables: vec![authors_table()],
                ..Default::default()
            }],
            ..Default::default()
        })
        .into(),
        queries,
        plugin_options: b"{}".to_vec(),
        sqlc_version: "1.0.0-test".to_string(),
        ..Default::default()
    };
    req.encode_to_vec()
}

fn assert_codegen_snapshot(snapshot_name: &str, code: &str) {
    let mut settings = insta::Settings::clone_current();
    settings.add_filter(
        r"// Code generated by sqlc-gen-sqlx v[0-9A-Za-z][0-9A-Za-z.+-]*\. DO NOT EDIT\.",
        "// Code generated by sqlc-gen-sqlx v[VERSION]. DO NOT EDIT.",
    );
    settings.add_filter(
        r"// sqlc version: [0-9A-Za-z][0-9A-Za-z.+-]*",
        "// sqlc version: [VERSION]",
    );
    settings.bind(|| {
        insta::assert_snapshot!(snapshot_name, code);
    });
}

#[test]
fn snapshot_one() {
    let query = Query {
        name: "GetAuthor".to_string(),
        cmd: ":one".to_string(),
        text: "SELECT id, name, bio FROM authors WHERE id = $1".to_string(),
        columns: vec![
            make_author_column("id", "int8", true),
            make_author_column("name", "text", true),
            make_author_column("bio", "text", false),
        ],
        params: vec![Parameter {
            number: 1,
            column: Some(make_author_column("id", "int8", true)).into(),
            ..Default::default()
        }],
        ..Default::default()
    };
    let bytes = make_request(vec![query]);
    let out_bytes = run_with_bytes(&bytes).expect("generate failed");
    let resp = sqlc_gen_sqlx::plugin::GenerateResponse::decode_from_slice(&out_bytes).unwrap();
    let code = String::from_utf8(resp.files[0].contents.clone()).unwrap();
    assert_codegen_snapshot("one", &code);
    assert!(
        code.contains("#![allow(\n    dead_code,\n    reason = \"generated queries may expose items a caller does not use\"\n)]"),
        "expected dead_code allow in:\n{code}"
    );
    assert!(
        code.contains("Queries<E>"),
        "expected Queries<E> in:\n{code}"
    );
}

