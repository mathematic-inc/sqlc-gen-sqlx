#[test]
fn domain_types_use_base_type() {
    // DOCUMENTS: sqlc pre-resolves domain types to their base type before
    // calling the plugin. A column defined as `email domain` in the schema
    // arrives with type "text" (the base type of the domain) in the
    // GenerateRequest. The plugin therefore never sees the domain name "email"
    // and does not need to handle domain resolution itself.
    //
    // This test simulates a pre-resolved domain: the column type is "text"
    // (what sqlc sends after resolving the "email" domain), and verifies that
    // the generated Rust type is `String`.
    use sqlc_gen_sqlx::plugin::{Catalog, Column, GenerateRequest, Identifier, Query, Schema};

    let query = Query {
        name: "GetEmail".to_string(),
        cmd: ":one".to_string(),
        text: "SELECT email FROM users WHERE id = $1".to_string(),
        columns: vec![Column {
            name: "email".to_string(),
            // Domain "email" pre-resolved to "text" by sqlc.
            r#type: Some(Identifier {
                name: "text".to_string(),
                ..Default::default()
            })
            .into(),
            not_null: true,
            ..Default::default()
        }],
        params: vec![],
        ..Default::default()
    };

    let req = GenerateRequest {
        catalog: Some(Catalog {
            default_schema: "public".to_string(),
            schemas: vec![Schema {
                name: "public".to_string(),
                ..Default::default()
            }],
            ..Default::default()
        })
        .into(),
        queries: vec![query],
        plugin_options: b"{}".to_vec(),
        sqlc_version: "1.0.0-test".to_string(),
        ..Default::default()
    };
    let bytes = req.encode_to_vec();
    let out_bytes = sqlc_gen_sqlx::run_with_bytes(&bytes).expect("generate failed");
    let resp = sqlc_gen_sqlx::plugin::GenerateResponse::decode_from_slice(&out_bytes).unwrap();
    let code = String::from_utf8(resp.files[0].contents.clone()).unwrap();
    // The email column should resolve to String (base type of the domain).
    assert!(
        code.contains("email: String"),
        "expected email: String (domain pre-resolved to text) in:\n{code}"
    );
}

